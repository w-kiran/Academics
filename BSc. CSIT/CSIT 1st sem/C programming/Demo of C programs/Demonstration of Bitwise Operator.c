/*** Demonstration Of Bitwise operator ***/
#include <stdio.h>
#include <conio.h>
int main()
{
	int a=7, b=5;
	printf(" a&b : %d\n", a&b); // Bitwise AND
	printf(" a|b : %d\n", a|b); // Bitwise OR
	printf(" a : %d\n", a); // COMPLEMENT
	printf(" a<<1 : %d\n", a<<1); // Left Shift
	printf(" b>>1 : %d\n", b>>1); // Bitwise OR
	
	
	
	return 0;
}
