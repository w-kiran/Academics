/***** Demonstration of increment and decrement operator *****/
#include <stdio.h>
#include <conio.h>
int main()
{
	int a=13, b=14;
	printf(" ++a : %d\n", ++a);
	printf(" a++ : %d\n", a++);
	printf(" a : %d\n", a);
	printf(" --b : %d\n", --b);
	printf(" b-- : %d\n", b--);
	printf(" b : %d\n", b);
	
	
	return 0;
}
